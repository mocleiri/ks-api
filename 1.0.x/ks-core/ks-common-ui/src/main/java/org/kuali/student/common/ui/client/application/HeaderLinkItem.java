public class HeaderLinkItem {
    
    String text;
    String title;
    String actionUrl;
    
    
    /**
     * This constructs a ...
     * 
     */
    public HeaderLinkItem() {
        super();
    }

    /**
     * This constructs a ...
     * 
     * @param text
     * @param title
     * @param actionUrl
     */
    public HeaderLinkItem(String text, String title, String actionUrl) {
        super();
        this.text = text;
        this.title = title;
        this.actionUrl = actionUrl;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getActionUrl() {
        return actionUrl;
    }

    public void setActionUrl(String actionUrl) {
        this.actionUrl = actionUrl;
    }


    
    

}
