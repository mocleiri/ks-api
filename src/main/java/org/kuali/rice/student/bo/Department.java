package org.kuali.rice.student.bo;

import java.util.LinkedHashMap;

import org.kuali.rice.kns.bo.PersistableBusinessObjectBase;

/**
 * Department Business Object.
 */
public class Department extends PersistableBusinessObjectBase {

	private static final long serialVersionUID = -709505184900197780L;

	private String departmentCode;
    private String departmentDescription;

    /**
     * Default constructor.
     */
    public Department() {

    }

    public String getDepartmentCode() {
        return departmentCode;
    }

    public void setDepartmentCode(String departmentCode) {
        this.departmentCode = departmentCode;
    }

    public String getDepartmentDescription() {
        return departmentDescription;
    }

    public void setDepartmentDescription(String departmentDescription) {
        this.departmentDescription = departmentDescription;
    }

    /**
     * @see org.kuali.rice.kns.bo.BusinessObjectBase#toStringMapper()
     */
    protected LinkedHashMap toStringMapper() {
        LinkedHashMap m = new LinkedHashMap();
        m.put("departmentCode", this.departmentCode);
        return m;
    }

}
